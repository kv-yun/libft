#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	char	*tab;

	if (!(tab = malloc(nmemb * size)))
		return (NULL);
	ft_bzero(tab, nmemb * size);
	return (tab);
}
