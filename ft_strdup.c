#include "libft.h"

char	*ft_strdup(const char *src)
{
	char		*cpy;
	size_t		i;
	size_t		size;

	if (src == NULL)
		return (NULL);
	size = ft_strlen(src);
	if (!(cpy = malloc((size + 1) * sizeof(char))))
		return (NULL);
	i = 0;
	while (src[i])
	{
		cpy[i] = src[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
